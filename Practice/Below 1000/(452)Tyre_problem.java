import java.io.*;
import java.util.*;
import java.lang.*;
class Codechef
{
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        for(int i=0;i<t;i++)
        {
            int n = sc.nextInt();
            int m = sc.nextInt();
            System.out.println((2*n)+(4*m));
        }
    }
}
